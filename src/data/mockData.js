export const filterProfiles = (profiles, filters) => {
  return profiles.filter(profile => {
    // Location filter
    if (filters.location && profile.location !== filters.location) {
      return false;
    }

    // Age range filter
    if (profile.age < filters.ageRange[0] || profile.age > filters.ageRange[1]) {
      return false;
    }

    // Price range filter
    if (profile.price < filters.priceRange[0] || profile.price > filters.priceRange[1]) {
      return false;
    }

    // Ethnicity filter
    if (filters.ethnicity && profile.ethnicity !== filters.ethnicity) {
      return false;
    }

    // Services filter
    if (filters.services.length > 0) {
      const hasService = filters.services.some(service => 
        profile.services.includes(service)
      );
      if (!hasService) return false;
    }

    // Languages filter
    if (filters.languages.length > 0) {
      const hasLanguage = filters.languages.some(language => 
        profile.languages.includes(language)
      );
      if (!hasLanguage) return false;
    }

    // Availability filter
    if (filters.availability === 'now' && !profile.isOnline) {
      return false;
    }

    // Special filters
    if (filters.verified && !profile.isVerified) {
      return false;
    }

    if (filters.vip && !profile.isVip) {
      return false;
    }

    if (filters.hasVideo && !profile.hasVideo) {
      return false;
    }

    return true;
  });
};

